Academic Year TEXT  COMMENT 'Academic Year',
County Code TEXT  COMMENT 'County Code',
District Code INT  COMMENT 'District Code',
School Code TEXT  COMMENT 'School Code',
County Name TEXT  COMMENT 'County Code',
District Name TEXT  COMMENT 'District Name',
School Name TEXT  COMMENT 'School Name',
District Type TEXT  COMMENT 'District Type',
School Type TEXT  COMMENT 'School Type',
Educational Option Type TEXT  COMMENT 'Educational Option Type',
NSLP Provision Status TEXT  COMMENT 'NSLP Provision Status',
Charter School (Y/N) INT  COMMENT 'Charter School (Y/N)',
Charter School Number TEXT  COMMENT 'Charter School Number',
Charter Funding Type TEXT  COMMENT 'Charter Funding Type',
IRC INT,
Low Grade TEXT  COMMENT 'Low Grade',
High Grade TEXT  COMMENT 'High Grade',
Enrollment (K-12) DECIMAL(10,2)  COMMENT 'Enrollment (K-12)',
Free Meal Count (K-12) DECIMAL(10,2)  COMMENT 'Free Meal Count (K-12)',
Percent (%) Eligible Free (K-12) DECIMAL(10,2),
FRPM Count (K-12) DECIMAL(10,2)  COMMENT 'Free or Reduced Price Meal Count (K-12)',
